public class Main {
    public static void main(String[] args) {
        // Print with newline
        System.out.println("Hello, World!");
        
        // Print without newline
        System.out.print("This is ");
        System.out.print("on the same line.");
        
        // Formatting output
        String name = "Alice";
        int age = 25;
        System.out.printf("Name: %s, Age: %d%n", name, age);
    }
}
