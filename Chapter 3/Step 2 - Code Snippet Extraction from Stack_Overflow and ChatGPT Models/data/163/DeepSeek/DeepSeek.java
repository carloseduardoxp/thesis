
public class DeepSeek {

    public static void main(String[] args) {

// Using String.valueOf()
        String intStr = String.valueOf(123);       // "123"
        String doubleStr = String.valueOf(12.34);  // "12.34"
        String boolStr = String.valueOf(true);     // "true"

// Using concatenation with empty string
        String floatStr = "" + 45.67f;             // "45.67"

// Using toString() for wrapper objects
        String longStr = Long.toString(100000L);   // "100000"

    }

}
