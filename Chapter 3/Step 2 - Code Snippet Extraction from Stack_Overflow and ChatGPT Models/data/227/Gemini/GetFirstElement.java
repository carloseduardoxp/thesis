public class GetFirstElement {
    public static void main(String[] args) {
        // Example with an integer array
        int[] numbers = {10, 20, 30, 40, 50};
        int firstNumber = numbers[0];
        System.out.println("First number: " + firstNumber); // Output: First number: 10

        // Example with a String array
        String[] colors = {"Red", "Green", "Blue"};
        String firstColor = colors[0];
        System.out.println("First color: " + firstColor); // Output: First color: Red
    }
}
