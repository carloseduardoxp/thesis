public class Gemini {

    public static void main(String[] args) {
        String firstName = "John";
        String lastName = "Doe";
        String fullName = firstName + " " + lastName;
        System.out.println(fullName); // Output: John Doe
        String age = " is " + 30;
        System.out.println(firstName + age); // Output: John is 30        
    }
}

