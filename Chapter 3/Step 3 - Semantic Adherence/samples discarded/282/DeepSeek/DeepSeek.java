
public class DeepSeek {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            if (i == 5) {  // Condition to break the loop
                System.out.println("Found 5 - breaking loop");
                break;
            }
            System.out.println("Current value: " + i);
        }

    }

}
